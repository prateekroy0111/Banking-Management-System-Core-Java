    public void showCustomerName()
    {
        try{
            String sql="select * from customer_details where Acc='"+username+"'";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            while(rs.next())
            {
                jLabel42.setText("Welcome  "+rs.getString("Name"));
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    public void showTransaction()
    {
        model=(DefaultTableModel)jTable2.getModel();
        try{
            String sql="select * from transaction_details where debit_acc='"+username+"' or credit_acc='"+username+"'";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            while(rs.next())
            {
                model.insertRow(model.getRowCount(),new Object[]{rs.getString("sr_no"),rs.getString("credit_acc"),rs.getString("debit_acc"),rs.getString("type"),rs.getString("amount"),rs.getString("date")});
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
