    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        // TODO add your handling code here:
        if(validationTransfer()==true){
            int bal=Integer.parseInt(jTextField21.getText());
            int trns=Integer.parseInt(jTextField22.getText());
            if(trns>bal){
                try{
                    jTextField22.setText("");
                    jPasswordField5.setText("");
                    jTextField22.requestFocus();
                    throw new LowBalanceException();
                }
                catch(Exception e){
                    JOptionPane.showMessageDialog(null, e);
                }
            }
            else{
                int newbal=bal-trns;
                String pin=jPasswordField5.getText();
                boolean q1,q2;
                String sql="select pin from login_details where acc='"+username+"' and pin='"+pin+"'";
                try{
                    pst=conn.prepareStatement(sql);
                    rs=pst.executeQuery();
                    if(rs.next()){
                        q1=updateBal(newbal);
                        q2=updateTransaction(); 
                        if(q1==true && q2==true){
                            JOptionPane.showMessageDialog(null, "Trasfered Successfully");
                            showBal();
                            jTextField18.setText("");
                            jTextField19.setText("");
                            jTextField20.setText("");
                            jTextField35.setText("");
                            jTextField36.setText("");
                            jTextField22.setText("");
                            jPasswordField5.setText("");
                        }
                        else{
                            JOptionPane.showMessageDialog(null, "error");
                        }
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "Incorrect Pin");
                    }
                }catch(Exception e){
                    JOptionPane.showMessageDialog(null, e);
                }
            }
        }
            
    }//GEN-LAST:event_jButton10ActionPerformed

